# Create a new variable with a value of 5
number_of_points = 10

# Display number of points we have
print("We have", number_of_points, "points")

# Start a new game and reset our points to 0
number_of_points = 0

print("At the start of the new game, we have", number_of_points, "points")

# What will our score be?
print("Our score will be: ")
print(number_of_points + 5)

# Score a goal and get five more points
number_of_points = number_of_points + 5

# Check our score
print("After scoring, we have", number_of_points, "points")

